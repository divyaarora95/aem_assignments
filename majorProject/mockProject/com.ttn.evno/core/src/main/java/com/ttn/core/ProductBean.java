package com.ttn.core;

public class ProductBean {
    private String reviewerName;
    private String productImage;
    private String reviewerImage;
    private String testimonialHeading;
    private String testimonialDescription;
    private String productLogo;
    
    public String getProductImage() {
        return productImage;
    }
    
    public void setProductImage(String productImage) {
        this.productImage = productImage;
    }
    
    public String getReviewerImage() {
        return reviewerImage;
    }
    
    public void setReviewerImage(String reviewerImage) {
        this.reviewerImage = reviewerImage;
    }
    
    public String getProductLogo() {
        return productLogo;
    }
    
    public void setProductLogo(String productLogo) {
        this.productLogo = productLogo;
    }
    
    
    public String getReviewerName() {
        return reviewerName;
    }
    
    public void setReviewerName(String reviewerName) {
        this.reviewerName = reviewerName;
    }
    
    public String getTestimonialHeading() {
        return testimonialHeading;
    }
    
    public void setTestimonialHeading(String testimonialHeading) {
        this.testimonialHeading = testimonialHeading;
    }
    
    public String getTestimonialDescription() {
        return testimonialDescription;
    }
    
    public void setTestimonialDescription(String testimonialDescription) {
        this.testimonialDescription = testimonialDescription;
    }
    
}
